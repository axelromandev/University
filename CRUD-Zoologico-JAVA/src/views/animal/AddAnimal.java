    public void validateData() {
        String name = txtName.getText();
        String nameplus = txtNamePlus.getText();
        String description = txtDescription.getText();
        String location = txtLocation.getText();
        if (name.isEmpty() || nameplus.isEmpty() || description.isEmpty() || location.isEmpty()) {
            JOptionPane.showMessageDialog(
                    null, "COMPLETA TODOS LOS CAMPOS.",
                    "HUBO UN PROBLEMA", JOptionPane.ERROR_MESSAGE);
            return;
        }
        methodsSql
                .register(name, nameplus, description, location);
        clear();
    }

    public void clear() {
        txtName.setText(null);
        txtNamePlus.setText(null);
        txtDescription.setText(null);
        txtLocation.setText(null);
    }

