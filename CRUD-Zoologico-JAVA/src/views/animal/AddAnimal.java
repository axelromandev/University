    private void txtNamePlusKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNamePlusKeyTyped
        int text = evt.getKeyChar();
        if ((text < 65 || text > 90) && (text < 97 || text > 122) && (text != 32)) {
            evt.consume();
        }
        if (txtNamePlus.getText().length() >= 45)
            evt.consume();
    }//GEN-LAST:event_txtNamePlusKeyTyped

